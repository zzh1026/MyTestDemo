package com.demo.downrefresh.test;

/**
 * ListView中item属性
 * 
 * @author w.w
 */
public class ItemInfo {

	/**
	 * id
	 */
	private int id;

	/**
	 * name
	 */
	private String name;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
